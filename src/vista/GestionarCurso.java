    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
        if (!txtf_id_curso.getText().equals("")) {
            int id = Integer.valueOf(txtf_id_curso.getText());
            cur.inUpDelCurso('b', id, null, null);
            GestionarCurso.tbl_cursos.setModel(new DefaultTableModel(null, new Object[]{"Identificador","Nombre","Créditos"}));
            cur.llenarJTCurso(GestionarCurso.tbl_cursos);
            txtf_id_curso.setText("");
            txtf_nombre_curso.setText("");
            spn_creditos.setValue(0);
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

