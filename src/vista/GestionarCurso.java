    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        if (!cur.cursoExiste(txtf_nombre_curso.getText())) {
            int id = Integer.valueOf(txtf_id_curso.getText());
            String etiqueta = txtf_nombre_curso.getText();
            int creditos = Integer.valueOf(spn_creditos.getValue().toString());
            cur.inUpDelCurso('e', id, etiqueta, creditos);
            GestionarCurso.tbl_cursos.setModel(new DefaultTableModel(null, new Object[]{"Identificador","Nombre","Créditos"}));
            cur.llenarJTCurso(GestionarCurso.tbl_cursos);
//            JOptionPane.showMessageDialog(null, "Curso editado");
            
        } else {
            JOptionPane.showMessageDialog(null, "El curso ya existe");
        }
    }//GEN-LAST:event_btn_editarActionPerformed

    private void tbl_cursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_cursosMouseClicked
        int index = tbl_cursos.getSelectedRow();
        txtf_id_curso.setText(tbl_cursos.getValueAt(index, 0).toString());
        txtf_nombre_curso.setText(tbl_cursos.getValueAt(index, 1).toString());
        spn_creditos.setValue(Integer.valueOf(tbl_cursos.getValueAt(index, 2).toString()));
    }//GEN-LAST:event_tbl_cursosMouseClicked

